<?php

namespace App\Http\Controllers;

use App\Models\Buku;
use App\Models\Genre;
use App\Models\Penerbit;
use App\Models\Pengarang;
use App\Models\RakBuku;

use Illuminate\Http\Request;

class BukuController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | VALIDASI ROLE
    |--------------------------------------------------------------------------
    |
    | Hanya admin & petugas
    |
    */

    private function checkRole()
    {
        if (!in_array(auth()->user()->role, ['admin', 'petugas'])) {

            abort(403);

        }
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN DATA BUKU
    |--------------------------------------------------------------------------
    */

    public function index(Request $request)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | SEARCH
        |--------------------------------------------------------------------------
        */

        $search = $request->search;

        /*
        |--------------------------------------------------------------------------
        | QUERY DATA
        |--------------------------------------------------------------------------
        */

        $buku = Buku::with([

                    'genre',
                    'pengarang',
                    'penerbit',
                    'rak'

                ])

                ->when($search, function ($query, $search) {

                    return $query->where('judul', 'like', '%' . $search . '%');

                })

                ->latest()

                ->paginate(5);

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view('buku.index', compact('buku'));
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN TAMBAH BUKU
    |--------------------------------------------------------------------------
    */

    public function create()
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | AMBIL MASTER DATA
        |--------------------------------------------------------------------------
        */

        $genre = Genre::all();

        $pengarang = Pengarang::all();

        $penerbit = Penerbit::all();

        $rak_buku = RakBuku::all();

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view('buku.create', compact(

            'genre',
            'pengarang',
            'penerbit',
            'rak_buku'

        ));
    }

    /*
    |--------------------------------------------------------------------------
    | SIMPAN DATA BUKU
    |--------------------------------------------------------------------------
    */

    public function store(Request $request)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | VALIDASI INPUT
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'id_genre' => 'required',

            'id_pengarang' => 'required',

            'id_penerbit' => 'required',

            'id_rak' => 'required',

            'judul' => 'required|max:255',

            'tahun' => 'required',

            'stok' => 'required|integer|min:0',

            'foto' => 'nullable|image|mimes:jpg,jpeg,png|max:2048'

        ]);

        /*
        |--------------------------------------------------------------------------
        | UPLOAD FOTO
        |--------------------------------------------------------------------------
        */

        $foto = null;

        if ($request->hasFile('foto')) {

            $foto = time() . '.' . $request->foto->extension();

            $request->foto->move(

                public_path('uploads/buku'),

                $foto

            );
        }

        /*
        |--------------------------------------------------------------------------
        | SIMPAN DATA
        |--------------------------------------------------------------------------
        */

        Buku::create([

            'id_genre' => $request->id_genre,

            'id_pengarang' => $request->id_pengarang,

            'id_penerbit' => $request->id_penerbit,

            'id_rak' => $request->id_rak,

            'judul' => $request->judul,

            'tahun' => $request->tahun,

            'stok' => $request->stok,

            'foto' => $foto

        ]);

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

                ->route('buku.index')

                ->with(

                    'success',

                    'Data buku berhasil ditambahkan'

                );
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN EDIT BUKU
    |--------------------------------------------------------------------------
    */

    public function edit($id)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA
        |--------------------------------------------------------------------------
        */

        $buku = Buku::findOrFail($id);

        $genre = Genre::all();

        $pengarang = Pengarang::all();

        $penerbit = Penerbit::all();

        $rak_buku = RakBuku::all();

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view('buku.edit', compact(

            'buku',

            'genre',

            'pengarang',

            'penerbit',

            'rak_buku'

        ));
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE DATA BUKU
    |--------------------------------------------------------------------------
    */

    public function update(Request $request, $id)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | VALIDASI INPUT
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'id_genre' => 'required',

            'id_pengarang' => 'required',

            'id_penerbit' => 'required',

            'id_rak' => 'required',

            'judul' => 'required|max:255',

            'tahun' => 'required',

            'stok' => 'required|integer|min:0',

            'foto' => 'nullable|image|mimes:jpg,jpeg,png|max:2048'

        ]);

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA
        |--------------------------------------------------------------------------
        */

        $buku = Buku::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | FOTO LAMA
        |--------------------------------------------------------------------------
        */

        $foto = $buku->foto;

        /*
        |--------------------------------------------------------------------------
        | UPDATE FOTO BARU
        |--------------------------------------------------------------------------
        */

        if ($request->hasFile('foto')) {

            $foto = time() . '.' . $request->foto->extension();

            $request->foto->move(

                public_path('uploads/buku'),

                $foto

            );
        }

        /*
        |--------------------------------------------------------------------------
        | UPDATE DATA
        |--------------------------------------------------------------------------
        */

        $buku->update([

            'id_genre' => $request->id_genre,

            'id_pengarang' => $request->id_pengarang,

            'id_penerbit' => $request->id_penerbit,

            'id_rak' => $request->id_rak,

            'judul' => $request->judul,

            'tahun' => $request->tahun,

            'stok' => $request->stok,

            'foto' => $foto

        ]);

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

                ->route('buku.index')

                ->with(

                    'success',

                    'Data buku berhasil diupdate'

                );
    }

    /*
    |--------------------------------------------------------------------------
    | HAPUS DATA BUKU
    |--------------------------------------------------------------------------
    */

    public function destroy($id)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        $this->checkRole();

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA
        |--------------------------------------------------------------------------
        */

        $buku = Buku::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | HAPUS DATA
        |--------------------------------------------------------------------------
        */

        $buku->delete();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

                ->route('buku.index')

                ->with(

                    'success',

                    'Data buku berhasil dihapus'

                );
    }
}