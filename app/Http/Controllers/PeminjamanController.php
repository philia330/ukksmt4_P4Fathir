<?php

namespace App\Http\Controllers;

use App\Models\Peminjaman;
use App\Models\Buku;
use Illuminate\Http\Request;

class PeminjamanController extends Controller
{
    /*
    |--------------------------------------------------------------------------
    | HALAMAN PEMINJAMAN
    |--------------------------------------------------------------------------
    */

    public function index()
    {
        // hanya admin & anggota
        if (!in_array(auth()->user()->role, ['admin', 'anggota'])) {

            abort(403);

        }

        // ambil data buku
        $bukus = Buku::latest()->paginate(8);

        return view('peminjaman.index', compact('bukus'));
    }

    /*
    |--------------------------------------------------------------------------
    | DETAIL BUKU
    |--------------------------------------------------------------------------
    */

    public function show($id)
    {
        // hanya admin & anggota
        if (!in_array(auth()->user()->role, ['admin', 'anggota'])) {

            abort(403);

        }

        // cari buku
        $buku = Buku::findOrFail($id);

        return view('peminjaman.show', compact('buku'));
    }

    /*
    |--------------------------------------------------------------------------
    | SIMPAN PEMINJAMAN
    |--------------------------------------------------------------------------
    */

    public function store(Request $request)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'id_buku' => 'required|exists:bukus,id',

        ]);

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA BUKU
        |--------------------------------------------------------------------------
        */

        $buku = Buku::findOrFail($request->id_buku);

        /*
        |--------------------------------------------------------------------------
        | CEK STOK
        |--------------------------------------------------------------------------
        */

        if ($buku->stok <= 0) {

            return back()->with(

                'error',

                'Stok buku habis'

            );
        }

        /*
        |--------------------------------------------------------------------------
        | SIMPAN PEMINJAMAN
        |--------------------------------------------------------------------------
        */

        Peminjaman::create([

            'user_id' => auth()->user()->id,

            'id_buku' => $buku->id,

            'tanggal_pinjam' => now(),

            'tanggal_kembali' => now()->addDays(7),

            // status awal
            'status' => 'menunggu',

            // default denda
            'denda' => 0,

        ]);

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->route('peminjaman.index')

            ->with(

                'success',

                'Peminjaman berhasil dikirim, menunggu konfirmasi petugas'

            );
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN TRANSAKSI
    |--------------------------------------------------------------------------
    */

    public function transaksi(Request $request)
    {
        // hanya admin & petugas
        if (!in_array(auth()->user()->role, ['admin', 'petugas'])) {

            abort(403);

        }

        /*
        |--------------------------------------------------------------------------
        | QUERY RELASI
        |--------------------------------------------------------------------------
        */

        $query = Peminjaman::with([

            'user',
            'buku'

        ]);

        /*
        |--------------------------------------------------------------------------
        | SEARCH
        |--------------------------------------------------------------------------
        */

        if ($request->search) {

            $query->whereHas('user', function ($q) use ($request) {

                $q->where(

                    'name',

                    'like',

                    '%' . $request->search . '%'

                );

            })

            ->orWhereHas('buku', function ($q) use ($request) {

                $q->where(

                    'judul',

                    'like',

                    '%' . $request->search . '%'

                );

            });
        }

        /*
        |--------------------------------------------------------------------------
        | PAGINATION
        |--------------------------------------------------------------------------
        */

        $peminjaman = $query->latest()->paginate(10);

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'peminjaman.transaksi',

            compact('peminjaman')

        );
    }

    /*
    |--------------------------------------------------------------------------
    | RIWAYAT PEMINJAMAN
    |--------------------------------------------------------------------------
    */

    public function riwayat()
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        if (!in_array(auth()->user()->role, ['admin', 'anggota'])) {

            abort(403);

        }

        /*
        |--------------------------------------------------------------------------
        | JIKA ADMIN
        |--------------------------------------------------------------------------
        */

        if (auth()->user()->role == 'admin') {

            $peminjaman = Peminjaman::with([

                'user',
                'buku'

            ])

            ->latest()

            ->paginate(10);

        }

        /*
        |--------------------------------------------------------------------------
        | JIKA ANGGOTA
        |--------------------------------------------------------------------------
        */

        else {

            $peminjaman = Peminjaman::with('buku')

                ->where('user_id', auth()->user()->id)

                ->latest()

                ->paginate(10);

        }

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'peminjaman.riwayat',

            compact('peminjaman')

        );
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN KONFIRMASI
    |--------------------------------------------------------------------------
    */

    public function konfirmasi($id)
    {
        // hanya admin & petugas
        if (!in_array(auth()->user()->role, ['admin', 'petugas'])) {

            abort(403);

        }

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA PEMINJAMAN
        |--------------------------------------------------------------------------
        */

        $peminjaman = Peminjaman::with([

            'user',
            'buku'

        ])->findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'peminjaman.konfirmasi',

            compact('peminjaman')

        );
    }

    /*
    |--------------------------------------------------------------------------
    | UPDATE KONFIRMASI
    |--------------------------------------------------------------------------
    */

    public function updateKonfirmasi(Request $request, $id)
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI
        |--------------------------------------------------------------------------
        */

        $request->validate([

            'status' => 'required|in:menunggu,dipinjam,dikembalikan,ditolak,pengembalian',

            'denda' => 'nullable|numeric|min:0',

        ]);

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA PEMINJAMAN
        |--------------------------------------------------------------------------
        */

        $peminjaman = Peminjaman::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA BUKU
        |--------------------------------------------------------------------------
        */

        $buku = Buku::findOrFail($peminjaman->id_buku);

        /*
        |--------------------------------------------------------------------------
        | MENUNGGU -> DIPINJAM
        |--------------------------------------------------------------------------
        */

        if (

            $peminjaman->status == 'menunggu'

            &&

            $request->status == 'dipinjam'

        ) {

            // kurangi stok
            $buku->stok -= 1;

            $buku->save();
        }

        /*
        |--------------------------------------------------------------------------
        | PENGEMBALIAN -> DIKEMBALIKAN
        |--------------------------------------------------------------------------
        */

        if (

            $peminjaman->status == 'pengembalian'

            &&

            $request->status == 'dikembalikan'

        ) {

            // tambah stok
            $buku->stok += 1;

            $buku->save();

            // simpan tanggal dikembalikan
            $peminjaman->tanggal_dikembalikan = now();
        }

        /*
        |--------------------------------------------------------------------------
        | UPDATE STATUS
        |--------------------------------------------------------------------------
        */

        $peminjaman->status = $request->status;

        /*
        |--------------------------------------------------------------------------
        | UPDATE DENDA
        |--------------------------------------------------------------------------
        */

        $peminjaman->denda = $request->denda ?? 0;

        /*
        |--------------------------------------------------------------------------
        | SIMPAN
        |--------------------------------------------------------------------------
        */

        $peminjaman->save();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->route('peminjaman.transaksi')

            ->with(

                'success',

                'Transaksi berhasil diperbarui'

            );
    }

    /*
    |--------------------------------------------------------------------------
    | HALAMAN PENGEMBALIAN
    |--------------------------------------------------------------------------
    */

    public function pengembalian()
    {
        /*
        |--------------------------------------------------------------------------
        | VALIDASI ROLE
        |--------------------------------------------------------------------------
        */

        if (!in_array(auth()->user()->role, ['admin', 'anggota'])) {

            abort(403);

        }

        /*
        |--------------------------------------------------------------------------
        | ADMIN
        |--------------------------------------------------------------------------
        */

        if (auth()->user()->role == 'admin') {

            $peminjaman = Peminjaman::with([

                'user',
                'buku'

            ])

            ->where('status', 'dipinjam')

            ->latest()

            ->get();

        }

        /*
        |--------------------------------------------------------------------------
        | ANGGOTA
        |--------------------------------------------------------------------------
        */

        else {

            $peminjaman = Peminjaman::with('buku')

                ->where('user_id', auth()->user()->id)

                ->where('status', 'dipinjam')

                ->latest()

                ->get();

        }

        /*
        |--------------------------------------------------------------------------
        | TAMPILKAN VIEW
        |--------------------------------------------------------------------------
        */

        return view(

            'peminjaman.pengembalian',

            compact('peminjaman')

        );
    }

    /*
    |--------------------------------------------------------------------------
    | AJUKAN PENGEMBALIAN
    |--------------------------------------------------------------------------
    */

    public function kembalikan($id)
    {
        /*
        |--------------------------------------------------------------------------
        | AMBIL DATA PEMINJAMAN
        |--------------------------------------------------------------------------
        */

        $peminjaman = Peminjaman::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | CEK STATUS
        |--------------------------------------------------------------------------
        */

        if ($peminjaman->status != 'dipinjam') {

            return back()->with(

                'error',

                'Buku tidak bisa dikembalikan'

            );
        }

        /*
        |--------------------------------------------------------------------------
        | UBAH STATUS
        |--------------------------------------------------------------------------
        |
        | Belum langsung dikembalikan
        | Menunggu verifikasi admin/petugas
        |
        */

        $peminjaman->status = 'pengembalian';

        $peminjaman->save();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->route('peminjaman.pengembalian')

            ->with(

                'success',

                'Pengajuan pengembalian berhasil dikirim'

            );
    }

    /*
    |--------------------------------------------------------------------------
    | HAPUS TRANSAKSI
    |--------------------------------------------------------------------------
    */

    public function destroy($id)
    {
        /*
        |--------------------------------------------------------------------------
        | CARI DATA
        |--------------------------------------------------------------------------
        */

        $peminjaman = Peminjaman::findOrFail($id);

        /*
        |--------------------------------------------------------------------------
        | JIKA STATUS MASIH DIPINJAM
        |--------------------------------------------------------------------------
        */

        if ($peminjaman->status == 'dipinjam') {

            $buku = Buku::findOrFail($peminjaman->id_buku);

            $buku->stok += 1;

            $buku->save();
        }

        /*
        |--------------------------------------------------------------------------
        | HAPUS DATA
        |--------------------------------------------------------------------------
        */

        $peminjaman->delete();

        /*
        |--------------------------------------------------------------------------
        | REDIRECT
        |--------------------------------------------------------------------------
        */

        return redirect()

            ->route('peminjaman.transaksi')

            ->with(

                'success',

                'Transaksi berhasil dihapus'

            );
    }
}